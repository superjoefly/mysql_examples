    <h1>MySQL Intro</h1>

    <p>MySQL is the most popular database system used with PHP.</p>

    <p>With PHP you can connect to and manage databases.</p>

    <hr />

    <h2>What is MySQL?</h2>

    <ul>
      <li>A database system used on the web</li>
      <li>A database system that runs on the server</li>
      <li>Ideal for both small and large applications</li>
      <li>Fast, reliable and easy to use</li>
      <li>Uses stadard SQL</li>
      <li>compiles on a number of platforms</li>
      <li>free</li>
      <li>developed, distributed and supported by Oracle Corporation</li>
    </ul>

    <p>The data in a mysql database are stored in tables. A table is a collection of related data, and it consists of columns and rows.</p>

    <p>Databases are useful for storing information categorically. A company may have a database with the following tables:</p>

    <ul>
      <li>Employees</li>
      <li>Products</li>
      <li>Customers</li>
      <li>Orders</li>
    </ul>

    <hr />

    <h2>PHP + MySQL Database System</h2>

    <p>PHP combined with MySQL are cross-platform (you can develop on Windows and serve on a Unix platform)</p>

    <hr />

    <h2>Database Queries</h2>

    <p>A query is a question or a request.</p>

    <p>We can query a database for specific information and have a recordset returned.</p>

    <p>Look at the following query (using standard SQL)</p>

    <pre><code>
      Select LastName FROM Employees
    </code></pre>

    <p>The query above selects all the data in the "LastName" column from the "Employees" table.</p>

    <hr />

    <h2>Facts About MySQL Database</h2>

    <p>MySQL is the de-facto standard database system for web sites with HUGE volumes of both data and end-users (like Facebook, Twitter and Wikipedia).</p>

    <p>Another great thing about MySQL is that it can be scaled down to support embedded database applications.</p>

